package ch10_Primitive_data_types;

/**
 * A boolean can store one of two values, either true or false.
 *
 * @author solcanm
 * @version 1.0
 * @since 2019-11-17
 */
public class BooleanPrimitive {

    public static void main(String[] args) {
        boolean trueVal = true;
        boolean falseval = false;
        boolean notTrue = !true;
        boolean trueAndFalse = true && false;
        boolean trueOrFalse = true || false;
        boolean trueXorFalse = true ^ false;
    }
}
